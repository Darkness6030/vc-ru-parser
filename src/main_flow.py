@router.message(UserState.url_select, F.text)
async def url_select_handler(message: Message, state: FSMContext):
    parsed_args = await utils.parse_url(message.text)
    if not parsed_args:
        return await message.answer('‚ùå –û—à–∏–±–∫–∞: –ù–µ–≤–µ—Ä–Ω—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã –∏–ª–∏ —Ñ–æ—Ä–º–∞—Ç URL. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑:')

    _, domain, username = parsed_args
    if not domain or not username:
        return await message.answer('‚ùå –û—à–∏–±–∫–∞: –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑:')

    if domain == 'tenchat.ru':
        user_data = await api.fetch_tenchat_user_data(username)
        if user_data['is_blocked']:
            return await message.answer(
                '‚ö†Ô∏è –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω. –í—Å—ë —Ä–∞–≤–Ω–æ –ø–∞—Ä—Å–∏–º?',
                reply_markup=InlineKeyboardBuilder()
                .button(text='–î–∞', callback_data=ParseBlockedConfirmCallback(mode=await state.get_value('mode'), username=username))
                .button(text='–ù–µ—Ç', callback_data=ParseBlockedCancelCallback())
                .as_markup()
            )

    match await state.get_value('mode'):
        case 'server':
            await load_server(message, state, domain, username)

        case 'sheets':
            await load_sheets(message, state, domain, username)


