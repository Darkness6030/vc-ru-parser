@router.message(UserState.add_account, F.text.regexp(r'^\S+\s+\S+$'))
async def add_account_input(message: Message, state: FSMContext):
    url, mode = message.text.split(maxsplit=2)
    if mode not in PARSING_MODES:
        return await message.answer(
            '‚ö†Ô∏è –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç. –ü—Ä–∏–º–µ—Ä: <code>https://dtf.ru/danny —Ç–∞–±–ª</code>'
        )

    parsed_args = await utils.parse_url(url)
    if not parsed_args:
        return await message.answer(
            '‚ùå –û—à–∏–±–∫–∞: –ù–µ–≤–µ—Ä–Ω—ã–µ –∞—Ä–≥—É–º–µ–Ω—Ç—ã –∏–ª–∏ —Ñ–æ—Ä–º–∞—Ç URL. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑:'
        )

    url, domain, username = parsed_args
    if not domain or not username:
        return await message.answer('‚ùå –û—à–∏–±–∫–∞: –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—è –Ω–µ —Å—É—â–µ—Å—Ç–≤—É–µ—Ç. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑:')

    user_data = await api.fetch_tenchat_user_data(username) \
        if domain == 'tenchat.ru' else \
        await api.fetch_user_data(domain, username)

    # if user_data['is_blocked']:
    #     return await message.answer('‚ùå –û—à–∏–±–∫–∞: –ü–æ–ª—å–∑–æ–≤–∞—Ç–µ–ª—å –∑–∞–±–ª–æ–∫–∏—Ä–æ–≤–∞–Ω. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑:')

    for account in storage.get_accounts():
        if account.url == url:
            return await message.answer('‚ùå –û—à–∏–±–∫–∞: –≠—Ç–æ—Ç –∞–∫–∫–∞—É–Ω—Ç —É–∂–µ –¥–æ–±–∞–≤–ª–µ–Ω. –ü–æ–ø—Ä–æ–±—É–π—Ç–µ –µ—â—ë —Ä–∞–∑:')

    storage.add_account(
        url=url,
        mode=mode,
        domain=domain,
        username=username,
        is_blocked=user_data['is_blocked']
    )

    await state.clear()
    await message.answer('‚úÖ –ê–∫–∫–∞—É–Ω—Ç –¥–æ–±–∞–≤–ª–µ–Ω!', reply_markup=regular_parsing_keyboard)


