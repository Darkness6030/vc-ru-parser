        total_deleted = len(all_deleted_posts)
        logger.warning(f'–û–±–Ω–∞—Ä—É–∂–µ–Ω–æ {total_deleted} —É–¥–∞–ª—ë–Ω–Ω—ã—Ö –ø–æ—Å—Ç–æ–≤')

