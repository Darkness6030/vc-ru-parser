@router.message(UserState.monitor_posts_periodicity, F.text)
async def monitor_posts_periodicity_input(message: Message, state: FSMContext):
    periodicity = []
    for line in message.text.splitlines():
        line = line.strip()
        if not line:
            continue

        try:
            parsed_time = datetime.strptime(line, '%H:%M').time()
            periodicity.append(parsed_time)
        except ValueError:
            await message.answer(f'‚ùå –ù–µ–≤–µ—Ä–Ω—ã–π —Ñ–æ—Ä–º–∞—Ç –≤—Ä–µ–º–µ–Ω–∏: `{line}`. –í–≤–µ–¥–∏—Ç–µ –≤—Ä–µ–º—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ –ß–ß:–ú–ú, –ø–æ –æ–¥–Ω–æ–º—É –Ω–∞ —Å—Ç—Ä–æ–∫—É.')
            return

    if not periodicity:
        await message.answer('‚ùå –ù–µ –Ω–∞–π–¥–µ–Ω–æ –Ω–∏ –æ–¥–Ω–æ–≥–æ –∫–æ—Ä—Ä–µ–∫—Ç–Ω–æ–≥–æ –≤—Ä–µ–º–µ–Ω–∏. –í–≤–µ–¥–∏—Ç–µ –≤—Ä–µ–º—è –≤ —Ñ–æ—Ä–º–∞—Ç–µ –ß–ß:–ú–ú, –ø–æ –æ–¥–Ω–æ–º—É –Ω–∞ —Å—Ç—Ä–æ–∫—É.')
        return

    posts_settings = storage.get_monitor_posts_settings()
    posts_settings.periodicity = periodicity
    storage.set_monitor_posts_settings(posts_settings)

    await state.clear()
    await message.answer('‚úÖ –ü–µ—Ä–∏–æ–¥–∏—á–Ω–æ—Å—Ç—å –æ–±–Ω–æ–≤–ª–µ–Ω–∞!', reply_markup=monitor_posts_keyboard)


